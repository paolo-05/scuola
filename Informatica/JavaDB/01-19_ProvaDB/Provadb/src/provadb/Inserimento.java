    private void btnInserisciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserisciActionPerformed
        // TODO add your handling code here:
         try {
            
           

            // Caricamento del driver MySQL
            Class.forName("com.mysql.jdbc.Driver");

            // Connessione al database Northwind
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:8889/northwind", "root", "root");

            // Creazione dell'oggetto per l'esecuzione della query
            Statement st = con.createStatement();
            
            // inserimento nella tabella
            String sql = "INSERT INTO customers (CustomerID, CompanyName, City, Country) " +
                   "VALUES ('" + txtID.getText() + "', '" + txtNome.getText() + "', '" + txtCitta.getText() + "', '" + txtPaese.getText() + "')";
            int nrow= st.executeUpdate(sql);
            if (nrow==1)
                JOptionPane.showMessageDialog(this,"Cliente inserito con successo!");
                
            else
                     JOptionPane.showMessageDialog(this,"Errore inserimento!");
            // Chiusura della connessione
            con.close();

        } catch (Exception e) {
                 JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_btnInserisciActionPerformed

