    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        Date data = (Date) spnData.getValue();
        Timestamp sqlTimestamp = new Timestamp(data.getTime());
        
        int fornitore = cmbProdotto.getSelectedIndex();
        int quantita = Integer.parseInt(spnQuantita.getValue().toString());
        String tipo = cmbTipo.getSelectedItem().toString();
        
        if(fornitore == 0 || quantita == 0 || tipo == "Seleziona"){
            JOptionPane.showMessageDialog(this, "Compila i campi poi invia", "Errore", 0);
            return;
        }
        
        try{
             db.insertMovement(sqlTimestamp, tipo, quantita, fornitore);
            JOptionPane.showMessageDialog(this, "Fornitore inserito correttamente", "Successo", 1);
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Errore nell'inserimento", "Errore", 0); 
        }
       
            
    }//GEN-LAST:event_btnAddMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // svuota map in caso di aggiornamenti sui fornitori
        prodotti.clear();
        
        cmbProdotto.removeAll();
        cmbProdotto.addItem("Seleziona");
        
        prodotti = db.getProdotti();
        prodotti.forEach((key, value) -> {
            cmbProdotto.addItem(value);
        });
        
        spnData.setValue(calendar.getTime());
   
    }//GEN-LAST:event_formWindowActivated

